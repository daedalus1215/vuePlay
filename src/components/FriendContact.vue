<template>
  <ul>
    <li>
      <h3>{{ name }} {{ isFavorite ? "(Favorite)" : "" }}</h3>
      <button @click="toggleDetails">Show Details</button>
      <button @click="toggleFavorites">Toggle Favorite</button>
      <ul v-if="detailsAreVisible">
        <li>Name: {{ fullName }}</li>
        <li>email: {{ email }}</li>
      </ul>
    </li>
  </ul>
</template>

<script>
export default {
  props: {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    isFavorite: {
      type: Boolean,
      required: false,
      default: false,
    },
  },
  emits: {
      'toggle-favorite': (id) => {
          if (id) {
              return true;
          } else {
              console.log('Id is missing');
              return false;
          }
      },
  },
  data() {
    return {
      detailsAreVisible: false,
    };
  },
  methods: {
    toggleDetails() {
      this.detailsAreVisible = !this.detailsAreVisible;
    },
    toggleFavorites() {
      this.$emit('toggle-favorite', this.name);
    },
  },
};
</script>

